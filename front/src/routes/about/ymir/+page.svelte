<h3 class="h3">What is Ymir:</h3>
<p class="my-3">
	Ymir is a primordial giant, closely linked to the creation myth and the beginning of the world in
	Norse mythology. A creature resulting from the dramatic encounter between ice and fire, he was fed
	by a cosmic cow and his body parts served as the building blocks of the universe.
</p>
<p class="my-3">
	He is able to beget offspring all by himself, one of his descendants being the mother of Odin.
	Odin and his brothers would end up killing him though, and they would shape all that exists out of
	his body. His scattered brains would become the clouds, and the borders of the known world would
	be encircled by his eyebrows.
</p>
